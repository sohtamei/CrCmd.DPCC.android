// FNumber
// type: CrDataType_UInt16
// value = F number * 100
enum class CrFnumber(val raw: Int) {
    IrisClose(0xFFFD), // Iris Close
    Unknown  (0xFFFE), // Display "--"
    Nothing  (0xFFFF); // Nothing to display
}

