// ShutterSpeed
// type: CrDataType_UInt32
// value: upper two bytes = numerator lower two bytes = denominator.
enum class CrShutterSpeed(val raw: Long) {
    Bulb(0x00000000),
    Nothing(0xFFFFFFFF); // Nothing to display
}

// IsoSensitivity
// type: CrDataType_UInt32
// value: bit 28-31 extension bit 24-27 ISO mode bit 0-23 ISO value
enum class CrISO(val raw: Int) {
    Normal(0x00 ),           // ISO setting Normal
    MultiFrameNR(0x01),      // Multi Frame NR
    MultiFrameNR_High(0x02), // Multi Frame NR High
    Ext(0x10),               // Indicates of extended value
    AUTO(0xFFFFFF);
}

fun describeDpOrCc(code: Int): String {
    DPC.fromRaw(code).takeIf { it != DPC.UNDEF }?.let { return it.name }
    PTP_CC.fromRaw(code)?.let { return it.name }
    return "(unknown)"
}
